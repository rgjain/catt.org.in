		<h1>CATT - Conservation Awareness Team Trust</h1>
		<div class="content">
			<p>We, the members of the CATT family, are professionals from various industries and sectors, united by a singular passion: the love for our wildlife and the urge to do our bit to save what is left of our natural heritage.</p>
			<p>We refuse to play mute spectators and sit idle, as our forests get decimated, wild animals get systematically eliminated, natural resources get exhausted and the environment gets polluted beyond repair. We refuse to indulge in irresponsible and unsustainable behavior that harms our environment and believe in educating others by spreading awareness about the same.</p>
			<p>CATT is a platform for action.</p>
			<p>What makes us effective is that our action isn't driven solely by emotions or misguided efforts. It is steered by conscious planning, thorough fieldwork and research, and leveraging a pool of diverse think tank powered by expertise, experience and willingness to embrace innovation. </p>
			<p>We are determined to work at every level - from grass-root to administration, to identify specific problems faced by our natural environs, and actually implement, re-assess and refine the solutions.</p>
			<p>There are many more like us, who want to do their bit and make our home a more beautiful place to live. CATT invites all such like-minded people to join hands and try to create a better planet. Together, we will make a difference.</p>
			<p>The time is NOW! Everyone must act. Or it might be too late.</p>

