	<h1>OUR MENTOR</h1>
	<div class="content">
		<p>Kantharaju H.C., Chief Conservator of Forest - Bandipur Tiger Reserve, Bandipur.</p>
		<img src="assets/images/profile/mentor/shrikantaraju.png">
		<p>Mr. Kantharaju H. C. is a 1985 batch Indian Forest Service(IFS) officer, currently posted as the Chief Conservator of Forests-Director of Project Tiger in Bandipur Tiger Reserve. He has been the chief mentor for CATT and has spearheaded many activities while steering CATT to achieve its agenda of wildlife conservation. CATT has been growing under the constant support, guidance and expertise of this most amicable and knowledgeable official of the Forest Department of Karnataka. CATT would not have grown up so much without the supervision and guidance of Mr. Kantharaju H.C. CATT is truly humbled by the cooperation it has received and will be grateful to him and his entire team of forest staffs forever.</p>
		<p>Mr. Kantharaju a native Kandigah hails from a village named Bilidegulea Hosur near Mandya, now settled in Mysore.</p>
